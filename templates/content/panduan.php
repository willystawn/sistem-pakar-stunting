<p>Panduan ini berisi informasi panduan dalam penginputan data diagnosis stunting pada balita:</p>
<p><span class="text-primary font-weight-bold">Nama: </span>Isi dengan nama lengkap balita.</p>
<p><span class="text-primary font-weight-bold">Jenis Kelamin: </span>Pilih jenis kelamin balita dengan memilih opsi yang
    tersedia (laki-laki atau perempuan).</p>
<p><span class="text-primary font-weight-bold">Usia (dalam bulan): </span>Isi dengan usia balita dalam bulan. Gunakan
    kalkulator usia di halaman ini untuk membantu Anda dalam menghitung
    usia balita dalam bulan.</p>
<p><span class="text-primary font-weight-bold">Berat Badan: </span>Isi dengan berat badan balita dalam satuan kilogram
    (kg).</p>
<p><span class="text-primary font-weight-bold">Tinggi Badan: </span>Isi dengan tinggi badan balita dalam satuan
    sentimeter (cm).</p>
<p>Pastikan semua data yang dimasukkan sudah benar dan akurat. Setelah itu, tekan tombol "Diagnosis" untuk mendapatkan
    hasil diagnosis dan rekomendasi tindakan yang harus dilakukan.</p>
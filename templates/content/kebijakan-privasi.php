<div class="font-weight-bold pb-2">Tujuan</div>
<p>Sistem pakar diagnosis stunting pada balita ini bertujuan untuk memberikan informasi yang akurat dan berguna tentang
    kondisi pertumbuhan dan perkembangan balita yang dapat membantu dalam diagnosis dan intervensi dini untuk mencegah
    atau memperbaiki stunting pada balita.</p>
<div class="font-weight-bold pb-2">Kepastian Diagnosis</div>
<p>Meskipun sistem pakar ini dapat memberikan diagnosis awal dan rekomendasi intervensi, pastikan untuk selalu
    memverifikasi hasil diagnosis dan rekomendasi dengan dokter atau profesional medis yang berkualifikasi untuk
    memastikan kepastian diagnosis.</p>
<div class="font-weight-bold pb-2">Informasi Pribadi</div>
<p>Sistem pakar diagnosis stunting pada balita ini dapat meminta informasi pribadi, seperti nama dan umur balita.
    Informasi ini hanya akan digunakan untuk tujuan yang sesuai dan tidak
    dibagikan atau digunakan untuk tujuan lain tanpa izin.</p>
<div class="font-weight-bold pb-2">Data yang Diperlukan</div>
<p>Pastikan bahwa data yang diperlukan untuk melakukan diagnosis stunting pada balita benar dan akurat. Sistem pakar ini
    mungkin memerlukan data seperti berat badan, tinggi badan, dan lain-lain untuk melakukan diagnosis.</p>
<div class="font-weight-bold pb-2">Tidak Menggantikan Konsultasi Medis</div>
<p>Meskipun sistem pakar diagnosis stunting pada balita ini dapat memberikan informasi yang akurat, sistem ini tidak
    dimaksudkan untuk menggantikan konsultasi medis dan pengobatan yang ditentukan oleh dokter atau profesional medis
    yang berkualifikasi.</p>
<div class="font-weight-bold pb-2">Tanggung Jawab Pengguna</div>
<p>Pengguna bertanggung jawab atas penggunaan sistem pakar ini dan keputusan yang dibuat berdasarkan hasil diagnosis dan
    rekomendasi. Pengguna juga bertanggung jawab untuk memastikan bahwa data yang dimasukkan ke dalam sistem adalah
    benar dan akurat.</p>
<div class="font-weight-bold pb-2">Perubahan Ketentuan Penggunaan</div>
<p>Ketentuan penggunaan sistem pakar diagnosis stunting pada balita ini dapat diubah sewaktu-waktu. Pastikan untuk
    memeriksa ketentuan penggunaan secara teratur dan memperbarui pengetahuan tentang sistem ini dan kebijakan
    penggunaannya.</p>